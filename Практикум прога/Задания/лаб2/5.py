def number_to_words(n):
    '''Функция для преобразования числа в слова (от 10 до 40)'''
    units = ["", "один", "два", "три", "четыре", "пять", 
             "шество", "семь", "восемь", "девять", "десять", 
             "одиннадцать", "двенадцать", "тринадцать", 
             "четырнадцать", "пятнадцать", "шестнадцать", 
             "семнадцать", "восемнадцать", "девятнадцать", 
             "двадцать", "двадцать один", "двадцать два", 
             "двадцать три", "двадцать четыре", 
             "двадцать пять", "двадцать шесть", 
             "двадцать семь", "двадцать восемь", 
             "двадцать девять", "тридцать", "тридцать один", 
             "тридцать два", "тридцать три", "тридцать четыре", 
             "тридцать пять", "тридцать шесть", 
             "тридцать семь", "тридцать восемь", 
             "тридцать девять", "сорок"]

    return units[n]

def get_task_description(count):
    '''Функция для формирования описания количества учебных заданий'''
    number_word = number_to_words(count)
    
    # Определяем, как согласовать слово "учебное задание"
    if count % 10 == 1 and count != 11:
        task_word = "учебное задание"
    elif count % 10 in [2, 3, 4] and not 12 <= count <= 14:
        task_word = "учебных задания"
    else:
        task_word = "учебных заданий"

    return f"{number_word} {task_word}"

# Ввод количества заданий
while True:
    try:
        count = int(input("Введите количество учебных заданий (от 10 до 40): "))
        if 10 <= count <= 40:
            print(get_task_description(count))
            break
        else:
            print("Ошибка: Введите число в диапазоне от 10 до 40.")
    except ValueError:
        print("Ошибка: Пожалуйста, введите целое число.")